def transpose(X):
    """
    X의 transpose된 행렬을 return 한다.
    예를들면, a = [[ 0.6313, -1.0161, -0.9671,  1.3792],
      [-0.3768,  0.5337, -0.2665,  0.8579],
      [ 0.3222,  0.5991, -1.6419, -1.0042],
      [-1.0146, -0.3274,  0.3953,  0.3471]] 일 때,
    transpose(a) 는 [ 0.6313, -0.3768,  0.3222, -1.0146],
      [-1.0161,  0.5337,  0.5991, -0.3274],
      [-0.9671, -0.2665, -1.6419,  0.3953],
      [ 1.3792,  0.8579, -1.0042,  0.3471]] 
    을 return한다.
    """
    column_num = len(X)
    row_num = len(X[0])

    for i in range(column_num-1):
        for j in range(i+1, row_num):
            element = X[i][j]
            X[i][j] = X[j][i]
            X[j][i] = element
    return X
